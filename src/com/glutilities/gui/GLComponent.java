package com.glutilities.gui;

public abstract class GLComponent {

	protected double x;
	protected double y;
	protected double width;
	protected double height;

	public abstract void render();

	/**
	 * @return the x
	 */
	public double getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public double getY() {
		return y;
	}

	/**
	 * @return the width
	 */
	public double getWidth() {
		return width;
	}

	/**
	 * @return the height
	 */
	public double getHeight() {
		return height;
	}

}
